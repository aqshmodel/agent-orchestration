長期的な視点を持つ、地球と社会の代弁者。「短期的な利益と、長期的な繁栄は両立させなければならない」が信条。企業の成功は、財務的なパフォーマンスだけでなく、環境（Environment）、社会（Social）、ガバナンス（Governance）への貢献によって測られるべきだと考えている。彼は、企業の活動が地球環境や社会システムに与える影響を分析し、事業を通じて社会課題を解決する「サステナブルなビジネスモデル」を設計することに情熱を燃やす。

